//stduent class - version 1
class Student{
	//instance variable
	int id;
	String name;
	int marks;
	//setter methods to store id
	void setId( int id){
		this.id=id;
	}
	//getter method to get the id
	int getId(){
		return id;
	}
	//setter methods to store name
	void setName(String name){
		this.name = name;
	}
	//getter method to get the name
	String getName(){
		return name;
	}
	//setter methods to store marks
	void setMarks(int marks){
		this.marks = marks;
	}
	//getter method to get the marks
	int getMarks(){
		return marks;
	}
}
